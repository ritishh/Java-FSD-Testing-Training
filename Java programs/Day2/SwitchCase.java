package Day2;

public class SwitchCase {

	public static void main(String[] args) 
	{
		
		int month = 10;
		switch(month)
		{
		case 1:
			System.out.println("january");
			return;
		case 2:
			System.out.println("feb");
			return;
		case 3:
			System.out.println("march");
			return;
			default:
				System.out.println("year end");
		}
	}

}
